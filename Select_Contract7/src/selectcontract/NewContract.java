    private void jSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSaveButtonActionPerformed
        // TODO add your handling code here:
        // validate the Contract ID
        String contractID = jTextContractID.getText().toUpperCase();      
        boolean contractAnswer = isValidContractID(contractID);
        
        String origin = (String) jOriginComboBox.getSelectedItem();
        String destination = (String) jDestComboBox.getSelectedItem();
        boolean locations = isLocationValid(origin, destination);
        
        String orderItem = jTextOrderItem.getText();
        boolean orderItemAnswer = isValidOrderItem(orderItem);
        
        System.out.println(contractID + ", " + origin + ", " + destination + ", " + orderItem);
        
        
//         add the other buttons
        if (contractAnswer && locations && orderItemAnswer){
//            String fileName = "/Users/manuel/Desktop/Select_Contract7/src/selectcontract/contracts.txt";
            String fileName = "E:\\Select_Contract7\\src\\selectcontract\\contracts.txt";
            
            try {
                BufferedWriter writer = new BufferedWriter(new FileWriter(fileName, true));
                writer.write(contractID + "," + origin + "," + destination + "," + orderItem);
                writer.newLine();
                writer.close();
                
            } catch (Exception ex) {
                System.out.println(ex);
            
            }
            
            System.out.println("clear");
        } else {
            System.out.println("not clear");
        }
       
       
    }//GEN-LAST:event_jSaveButtonActionPerformed
    
    
