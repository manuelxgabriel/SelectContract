    private boolean isValidContractID(String id){
        
        boolean theTruth = true;
        
        // validates if the input is 4 characters
        if (id.length() != 4){
            theTruth = false;
            JOptionPane.showMessageDialog(this, "Error: not enough character");
        } else if (!Character.isDigit(id.charAt(0))) {
            theTruth = false;
            JOptionPane.showMessageDialog(this, "Error: First chracter need to be a number");
        } else {
            // check if the other charactes are letters
            for(int i = 1; i < 4; i++){
                char c = id.charAt(i);
                if (!Character.isLetter(c)){
                    theTruth = false;
                    JOptionPane.showMessageDialog(this, "Error: There is more than one number");
                }
            }
        }
        
       if(theContracts.containsId(id)){
           theTruth = false;
           JOptionPane.showMessageDialog(this, "Error: There is duplication");
       }
        
        
        
        return theTruth;
    }
    
    private boolean isLocationValid(String origin, String destination){
        boolean validLocation = true;
                
        if (origin.equals(destination)){
            validLocation = false;
            JOptionPane.showMessageDialog(this, 
                    "Error Destination: Destination and Origin are the same location");
        }
    
    return validLocation;
    }
    
    private boolean isValidOrderItem(String order){
        boolean orderValid = true;
        
        if (order.contains(",") || !order.chars().allMatch(Character::isLetter)){
            orderValid = false;
            JOptionPane.showMessageDialog(this,
                    "Error Order Item: contains a comma or numbers");
        }
        if(order.equals("")){
            orderValid = false;
            JOptionPane.showMessageDialog(this,"Error Order Item: is empty");
        }
        
    return orderValid;
    }

